<h2 id="mission_statement">Mission Statement</h2>
<p>China Fund Seeker is an initiative committed to assist China’s nonprofits and individuals in finding global and local resources in developing their projects and participate in their capacity building of securing those resources. We are also committed to provide comprehensive, timely and consolidated resource information to the stakeholders of China’s social development.</p>
